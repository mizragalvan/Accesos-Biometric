package mx.pagos.admc.contracts.structures;

public class ModifiedClausules {
	private Integer idModifiedClause;
	private String previousClause;
	private String newClause;
	private String nameClause;
	private String numberClause;
	private Integer IdRequisition;
	public Integer getIdModifiedClause() {
		return idModifiedClause;
	}
	public void setIdModifiedClause(Integer idModifiedClause) {
		this.idModifiedClause = idModifiedClause;
	}
	public String getPreviousClause() {
		return previousClause;
	}
	public void setPreviousClause(String previousClause) {
		this.previousClause = previousClause;
	}
	public String getNewClause() {
		return newClause;
	}
	public void setNewClause(String newClause) {
		this.newClause = newClause;
	}
	public String getNumberClause() {
		return numberClause;
	}
	public void setNumberClause(String numberClause) {
		this.numberClause = numberClause;
	}
	public Integer getIdRequisition() {
		return IdRequisition;
	}
	public void setIdRequisition(Integer idRequisition) {
		IdRequisition = idRequisition;
	}
	public String getNameClause() {
		return nameClause;
	}
	public void setNameClause(String nameClause) {
		this.nameClause = nameClause;
	}
	
	

}
